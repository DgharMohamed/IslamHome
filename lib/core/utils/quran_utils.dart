  static bool isArabic(String text) {
    return RegExp(r'[\u0600-\u06FF]').hasMatch(text);
  }

  /// إزالة الحركات (التشكيل) من النص العربي
  /// يستخدم لتسريع البحث في أسماء السور
  static String removeDiacritics(String text) {
    // الحركات العربية (التشكيل)
    const diacritics = [
      '\u064B', // تنوين فتح
      '\u064C', // تنوين ضم
      '\u064D', // تنوين كسر
      '\u064E', // فتحة
      '\u064F', // ضمة
      '\u0650', // كسرة
      '\u0651', // شدة
      '\u0652', // سكون
      '\u0653', // مدة
      '\u0654', // همزة فوق
      '\u0655', // همزة تحت
      '\u0656', // ألف صغيرة
      '\u0657', // ألف واصلة
      '\u0658', // علامة نون صغيرة
      '\u0670', // ألف خنجرية
    ];

    String result = text;
    for (var diacritic in diacritics) {
      result = result.replaceAll(diacritic, '');
    }
    return result;
  }

  /// توحيد الأحرف العربية المتشابهة (Normalization) لغرض البحث
  static String normalizeArabic(String text) {
    String result = removeDiacritics(text);

    // توحيد الألفات
    result = result.replaceAll(RegExp(r'[أإآ]'), 'ا');
    // توحيد الياء
    result = result.replaceAll(RegExp(r'[ى]'), 'ي');
    // توحيد التاء المربوطة
    result = result.replaceAll(RegExp(r'[ة]'), 'ه');

    return result.trim();
  }
